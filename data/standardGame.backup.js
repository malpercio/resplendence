const test = {
cards: [],
nobles: []};
for(let card of module.exports.cards){
  const newCost = {};
  for (let cost of card.cost){
    newCost[cost.gem] = cost.cost;
  }
  for (let gem of gems){
    newCost[gem] = newCost[gem] || 0;
  }
  card.cost = newCost;
  test.cards.push(card);
}
for(let noble of module.exports.nobles){
  const newCost = {};
  for (let cost of noble.cost){
    newCost[cost.gem] = cost.count;
  }
  for (let gem of gems){
    newCost[gem] = newCost[gem] || 0;
  }
  noble.cost = newCost;
  test.nobles.push(noble)
}
